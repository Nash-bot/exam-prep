#include <unistd.h>

int main(int ac, char **av)
{
	int i;
	int result;
	
	i = 0;
	if(ac != 2)
	{
		write(1, "a\n",1);
	}
	while(av[1][i] != '\0' && result == 0)
	{
		if(av[1][i] == 'a')
		{	
		write(1,"a",1);
		result = 1;
		}	
		i++;
	}
	if(result == 0)
	write(1, "\n", 1);
	return(0);
}
